int	ft_is_prime(int nb)
{
	int i;
	int count;

	count = 0;
	i = 0;
	while (++i <= nb / 2)
	{
		if ((nb / i) * i == nb)
			count++;
	}
	if (count == 1)
		return (1);
	else
		return (0);
}
